package ru.kirill.foxgame.model;


/**
 * Представляет достоинство карты в игре "Лисица на опушке".
 * Значения от 1 до 11, где нечетные карты обладают особыми эффектами.
 * 
 * <p>Достоинство определяет силу карты при сравнении в рамках одной масти.
 * Нечетные достоинства (1, 3, 5, 7, 9, 11) имеют особые эффекты,
 * описанные в классе {@link CardEffect}.
 * 
 * @see Card
 * @see CardEffect
 */
public enum Rank {
    /**
     * Карта достоинством 1 - Лебедь.
     * Эффект: меняет следующего ведущего при определенных условиях.
     */
    ONE(1, "1"),

    /**
     * Карта достоинством 2.
     * Не имеет особого эффекта.
     */
    TWO(2, "2"),

    /**
     * Карта достоинством 3 - Лиса.
     * Эффект: меняет козырную масть.
     */
    THREE(3, "3"),

    /**
     * Карта достоинством 4.
     * Не имеет особого эффекта.
     */
    FOUR(4, "4"),

    /**
     * Карта достоинством 5 - Дровосек.
     * Эффект: заставляет соперника сбросить случайную карту.
     */
    FIVE(5, "5"),

    /**
     * Карта достоинством 6.
     * Не имеет особого эффекта.
     */
    SIX(6, "6"),

    /**
     * Карта достоинством 7 - Страж.
     * Эффект: блокирует эффекты других карт.
     */
    SEVEN(7, "7"),

    /**
     * Карта достоинством 8.
     * Не имеет особого эффекта.
     */
    EIGHT(8, "8"),

    /**
     * Карта достоинством 9 - Ведьма.
     * Эффект: становится козырем, если она одна на столе.
     */
    NINE(9, "9"),

    /**
     * Карта достоинством 10.
     * Не имеет особого эффекта.
     */
    TEN(10, "10"),

    /**
     * Карта достоинством 11 - Лунатик.
     * Эффект: обмен случайными картами с соперником.
     */
    ELEVEN(11, "11");
    
    private final int value;
    private final String symbol;

    /**
     * Создает достоинство карты.
     *
     * @param value числовое значение
     * @param symbol символьное представление
     */
    Rank(int value, String symbol) {
        this.value = value;
        this.symbol = symbol;
    }

    /**
     * Возвращает числовое значение достоинства.
     *
     * @return значение от 1 до 11
     */
    public int getValue() {
        return value;
    }
    
    /**
     * Возвращает символьное представление достоинства.
     * 
     * @return строковое представление достоинства
     */
    @Override
    public String toString() {
        return symbol;
    }
}
